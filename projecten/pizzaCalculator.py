small = 6
medium = 9
large = 11

antwoord_welke_pizza = input('Hallo, wilt een small, medium of een large pizza? ')
antwoord_hoeveel_pizza = int(input('En hoeveel stuks zou je er willen hebben'))

if antwoord_welke_pizza == 'small':
    input(small * antwoord_hoeveel_pizza)

if antwoord_welke_pizza == 'medium':
    input(medium * antwoord_hoeveel_pizza)

if antwoord_welke_pizza == 'large':
    input(large * antwoord_hoeveel_pizza)



 




